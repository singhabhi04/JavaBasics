package JavaBasicConcept;

public class DataTypesDemo {

	int number;
	boolean status;
	char ch;
	long l;
	float f;
	double d;

	public static void main(String[] args) {
		DataTypesDemo dtypes = new DataTypesDemo();
		System.out.println(dtypes.ch);
		System.out.println(dtypes.number);
		System.out.println(dtypes.status);
		System.out.println(dtypes.l);
		System.out.println(dtypes.d);
		System.out.println(dtypes.f);

		// byte-->short-->int/char-->long-->float-->double

	}

}
